      <p>栃木県那須塩原市のダム湖において、午後2時25分ごろ、クマ1頭が約300メートルにわたって泳いでいる姿が目撃された 20。この目撃情報は、クマが水域を障壁として認識せず、むしろ移動経路として積極的に活用している可能性を示唆している 20。ダム周辺での目撃は前日や11時間前にも報告されており、当該個体が水陸両用のルートを使いながら広範囲に移動していると分析される 20。</p>
