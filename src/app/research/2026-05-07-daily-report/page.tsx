      <p>大阪府北部で見られる流入個体への懸念に見られるように、クマの管理はもはや一つの自治体で完結するものではない 10。兵庫県や京都府からの流入を食い止めるためには、都道府県を越えた広域的な個体数管理（キャリング・キャパシティの調整）が不可欠である。2026年には「クママップ」や「クマダス」といったデジタルプラットフォームを活用したリアルタイムの情報共有が進んでいるが、これをいかにして迅速な「実効的なアクション（捕獲・追い払い）」に繋げるかが今後の焦点となる 4。</p>
